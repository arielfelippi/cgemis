    <h6 style="text-align:justify">
        O presente artigo apresenta a descrição da ferramenta C-Gemis, a qual executa análises de dados do câncer gástrico obtidos em bancos de dados públicos, TCAG e GEO. O principal objetivo do C-Gemis é realizar análises estáticas com maior riqueza de detalhes em comparação a outras ferramentas similares já disponíveis. Assim, busca-se potencializar a análise de dados de experimentos em microarranjos e RNA-Seq. O C-Gemis realiza análises relacionando os dados com as classificações tumorais já descritas em literatura, como: Laurén, WHO, TCGA. Adicionalmente, também realiza análise de sobrevida, baseados em dados clínicos disponibilizados pelo TCGA
    </h6>

